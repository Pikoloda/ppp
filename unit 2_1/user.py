number_int1 = int(input("Cześć podaj pierwszą liczbę całkowitą: "))
number_int2 = int(input("Cześć podaj drugą liczbę całkowitą: "))
dodawanie = number_int1 + number_int2
odejmowanie = number_int1 - number_int2
mnozenie = number_int1 * number_int2
dzielenie = number_int1 / number_int2
modulo = number_int1 % number_int2

print("Wynik dodawania: "+ str(dodawanie))
print("Wynik odejmowania: "+ str(odejmowanie))
print("Wynik mnożenia: "+ str(mnozenie))
print("Wynik dzielenia: "+ str(dzielenie))
print("Wynik modulo: "+ str(modulo))


